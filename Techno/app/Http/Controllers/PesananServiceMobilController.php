    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      $this->validate($request,[
        'jangka' =>'required',
        'email' => 'required',
        'category' => 'required'
        ]);
        $service = new Service;
        $service->email = $request->email;
        $service->alamat = $request->alamat;
        $service->kategori = $request->category;
        $service->jenis = $request->jenis;
        $service->jangkaWaktu = $request->jangka;

        $service->save();
        return redirect()->route('pesananServiceMobil.index')->with('alert-success','Pesanan telah ditambahkan!');
    }

