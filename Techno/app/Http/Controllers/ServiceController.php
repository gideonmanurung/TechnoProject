     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      $kategori = $request->category;
      $jenis = $request->jenis;
      if($kategori=="Lengkap"||$kategori=="Ringan"||$kategori=="Berat"&&$jenis=="Motor")
      {
        return view('pesanan.layananservicemotor',['category' =>$kategori,'jenis'=>$jenis]);
      }
      if($kategori=="Lengkap"||$kategori=="Ringan"||$kategori=="Berat"&&$jenis=="Mobil")
      {
        return view('pesanan.layananservicemobil',['category' =>$kategori,'jenis'=>$jenis]);
      }
    }

